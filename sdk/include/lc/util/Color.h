    //! Get color from name
    static Color fromName(const char* colorName);

    //! Get color name
    std::string name(bool forceHex = false) const;

    //! Get red value (0 - 255)
    uint8_t red() const { return red_; }

    //! Get green value (0 - 255)
    uint8_t green() const { return green_; }

    //! Get blue value (0 - 255)
    uint8_t blue() const { return blue_; }

    //! Get alpha value (0 - 255)
    uint8_t alpha() const { return alpha_; }

    //! Set red value
    void setRed(uint8_t red) { red_ = red; }

    //! Set green value
    void setGreen(uint8_t green) { green_ = green; }

    //! Set blue value
    void setBlue(uint8_t blue) { blue_ = blue; }

    //! Set alpha value
    void setAlpha(uint8_t alpha) { alpha_ = alpha; }

    //! Comparison
    bool operator==(const Color& other) const;

    //! Comparison
    bool operator!=(const Color& other) const { return !(*this == other); }

    // Compare two colors (including alpha value)
    bool equals(const Color& c) const { return *this == c; }

    //! Compare two colors (disregarding alpha value)
    bool equalsNoAlpha(const Color& clr) const;

    //! Get color without alpha (opaque)
    Color noAlpha() const { return {red_, green_, blue_}; }

    //! Get inverse color
    Color inverse() const;

    //! Get squared distance between two colors
    double distance(Color other, bool ignoreAlpha) const;

