//------------------------------------------------------------------------------
//! Blend two colors
inline Color blend(Color fgClr, Color bgClr)
{
    struct C
    {
        double R, G, B, A;
    };
    C r;
    C fg = {fgClr.red() / 255.0, fgClr.green() / 255.0, fgClr.blue() / 255.0,
            fgClr.alpha() / 255.0};
    C bg = {bgClr.red() / 255.0, bgClr.green() / 255.0, bgClr.blue() / 255.0,
            bgClr.alpha() / 255.0};
    r.A = 1 - (1 - fg.A) * (1 - bg.A);
    r.R = fg.R * fg.A / r.A + bg.R * bg.A * (1 - fg.A) / r.A;
    r.G = fg.G * fg.A / r.A + bg.G * bg.A * (1 - fg.A) / r.A;
    r.B = fg.B * fg.A / r.A + bg.B * bg.A * (1 - fg.A) / r.A;
    return {static_cast<uint8_t>(r.R * 255.0), static_cast<uint8_t>(r.G * 255.0),
            static_cast<uint8_t>(r.B * 255.0), static_cast<uint8_t>(r.A * 255.0)};
}

//------------------------------------------------------------------------------
inline bool Color::operator==(const Color& other) const
{
    return red_ == other.red_ && green_ == other.green_ && blue_ == other.blue_ &&
           alpha_ == other.alpha_;
}

//------------------------------------------------------------------------------
inline bool Color::equalsNoAlpha(const Color& clr) const
{
    return red_ == clr.red_ && green_ == clr.green_ && blue_ == clr.blue_;
}

//------------------------------------------------------------------------------
inline Color Color::inverse() const
{
    return {static_cast<uint8_t>(0xff - red_), static_cast<uint8_t>(0xff - green_),
            static_cast<uint8_t>(0xff - blue_), alpha_};
}

//------------------------------------------------------------------------------
constexpr Color Color::fromRGBA(uint32_t value)
{
    return {static_cast<uint8_t>(value & 0xff), static_cast<uint8_t>(value >> 8 & 0xff),
            static_cast<uint8_t>(value >> 16 & 0xff), static_cast<uint8_t>(value >> 24 & 0xff)};
}

//------------------------------------------------------------------------------
inline uint32_t Color::asRGBA() const
{
    return static_cast<uint32_t>(alpha_) << 24 | static_cast<uint32_t>(blue_) << 16 |
           static_cast<uint32_t>(green_) << 8 | static_cast<uint32_t>(red_);
}

